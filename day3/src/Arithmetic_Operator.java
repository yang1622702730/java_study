public class Arithmetic_Operator {
    public static void main(String[] args) {
        int num1 = 123;
        System.out.println("个位数为" + num1 % 10);
        System.out.println("十位数为" + num1 / 10 % 10);
        System.out.println("百位数为" + num1 / 100);
        int num3 = 1;
        int num2 = 2;
        boolean result = (num2 > num3) ? true : false;
        System.out.println(result);

    }
}
